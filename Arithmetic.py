"""
Arithmetic Operators:
1. + (Addition)
2. - (Subtraction)
3. * (Multiplication)
4. / (Division)
5. % (Remainder)
6. // (floor division)
7. ** (Exponent)
"""
a = 15
b = 10

print("Addition: ",a+b)
print("Subtraction: ",a-b)
print("Multiplication: ",a*b)
print("Division: ",a/b)
print("Remainder: ",a%b)
print("Floor division: ",a//b)
print("Exponent: ",a**b)                   # 5 ** 2 ---> base ** power ---> 5 ^ 2 = 25